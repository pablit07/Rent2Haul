                                        <div class="extra-box margin-top">
                                             Although Ufirst Moving will take every necessary precaution on your move, unexpected damage can occur and it is important to make sure that your possessions are protected. We are able to offer valuation (moving insurance) to our customers at a very fair price due to the very low number of claims we file.
